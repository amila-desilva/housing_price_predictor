# Goal: Create a class called circle with a method that will return the diameter of a circle, given the radius

class Circle:
    def __init__(self):
        pass
    
    def diameter(radius):
        """
        Given the radius of a circle, this function returns the diameter of that circle.

        :param radius: The radius of a circle
        :type radius: int
        :return diameter: The diameter, two times the radius
        :rtype: int
        """
        return 2 * radius